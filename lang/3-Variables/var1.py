# Variable Calculations
x = 2
y = 3
print( "x =", x )
print( "y =", y )
print( "x * y =", x * y )
print( "x + y =", x + y )

# Variable Calculations (SWAP) 
x = 2
y = 3
print( "x =", x, "and y =", y )
# Swap the values of x and y using z as intermediary storage.
z = x
x = y
y = z
print( "x =", x, "and y =", y )
